int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	if (0 == n)
		return (0);
	i = 0;
	while (i < n && *s1 && *s2 && *s1 == *s2)
	{
		++s1;
		++s2;
		++i;
	}
	if (i < n)
		return (*s1 - *s2);
	return (0);
}
